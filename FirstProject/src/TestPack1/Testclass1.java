package TestPack1;

public class Testclass1 {

	
	public static void main(String[] args) {
		System.out.println("Hello World...");
		
		byte byteNum = 50;  // byte number (primitive)
		System.out.println(byteNum);
		
		short shortNum = 900;  // short number (primitive)
		System.out.println(shortNum);
		
		int intNum = 800;  // int (primitive)
		System.out.println(intNum);
		
		long longNum = 983749489;  // long numbers 
		System.out.println(longNum);
		
		float floatNum = 9428.852f;  // float number 
		System.out.println(floatNum);
		
		double doubleNum = 7412210.65115241;  // big numbers  
		System.out.println(doubleNum);
		
		char charName = '@';  // only unique characters or 1 alphabet
		System.out.println(charName);
		
		boolean result = true; 
		boolean result2 = false;  // only true or false
		System.out.println(result);
		System.out.println(result2);
		
		}
		
}
